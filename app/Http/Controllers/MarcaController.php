<?php

namespace App\Http\Controllers;

use App\Models\Marca;
use Illuminate\Http\Request;

class MarcaController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $marca = new Marca ();

        if($request->has('sortBy')){
            if($request->get('sortDesc') === 'true'){
                $marca = $marca->orderBy($request->get('sortBy'), 'desc');
            }else{
                $marca = $marca->orderBy($request->get('sortBy'), 'asc');
            }
        }else{
            $marca = $marca->orderBy('id', 'desc');
        }

        if($request->has('itemsPerPage')){
            $itemsPerPage = $request->get('itemsPerPage');
        }else{
            $itemsPerPage = 15;
        }

        $search = $request->get('search');

        $marca = $marca->where('casa_marca', 'LIKE', "%$search%")
        ->orderBy('casa_marca', 'desc')
        ->paginate($itemsPerPage);

        return [
            'marca' => $marca,
        ];
    }

    /**
